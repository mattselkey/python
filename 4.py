def main():
    x, y = 100, 100
#conditional flow uses if, elif, else  
    if (x < y):
        st = "x is less than y" 
    else:
        st = "x is great than y"
        print (st)

  # conditional statements let you use "a if C else b"
  

if __name__ == "__main__":
  main()